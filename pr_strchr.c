#include "libftprintf.h"

char	*pr_strchr(char *s, int c)
{
	int		i;

	i = 0;
	while (*(s + i))
	{
		if (c == *(s + i))
			return (&s[i]);
		else
			i++;
	}
	if (c == '\0')
		return (&s[i]);
	return (NULL);
}
